package CursoJava.secao5_EstruturaCondicional.condicionais;

//A estrutura condicional if indica uma sequência de código que só é executada
//quando a condição indicada é verdadeira

public class If {
    public static void main(String[] args) {

        int x = 5;

        if (x > 0) { //condição a ser testada
            System.out.println("Bom dia!");
        }

        System.out.println("Fim do programa");

    }
}
